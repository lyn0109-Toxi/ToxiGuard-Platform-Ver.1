        row("성능/동등성", evidence("bioequivalence", "용출/동등성 근거 부족"), "대조약 대비 용출률, f2, bootstrap 또는 동등성 판단 근거가 충분한가?")
        row("안정성", evidence("stability", "안정성 근거 부족"), "가속/장기/포장 조건이 shelf-life와 직접 연결되는가?")
