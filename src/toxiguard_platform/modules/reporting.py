def create_pdf_report(payload: dict, language: str = "en") -> bytes:
    """Create a CTD-style regulatory assessment PDF."""
    try:
        from reportlab.lib import colors
        from reportlab.lib.enums import TA_CENTER, TA_LEFT
        from reportlab.lib.pagesizes import letter
        from reportlab.lib.styles import ParagraphStyle, getSampleStyleSheet
        from reportlab.lib.units import inch
        from reportlab.pdfbase import pdfmetrics
        from reportlab.pdfbase.cidfonts import UnicodeCIDFont
        from reportlab.pdfbase.ttfonts import TTFont
        from reportlab.platypus import (
            KeepTogether,
            PageBreak,
            Paragraph,
            SimpleDocTemplate,
            Spacer,
            Table,
            TableStyle,
        )
    except ImportError:
        return _minimal_pdf_stub(payload)

    language = "ko" if language == "ko" else "en"
    label = LABELS[language]
    font_name, bold_font = _register_report_fonts(pdfmetrics, TTFont, UnicodeCIDFont, language)

    buffer = io.BytesIO()
    doc = SimpleDocTemplate(
        buffer,
        pagesize=letter,
        leftMargin=0.62 * inch,
        rightMargin=0.62 * inch,
        topMargin=0.72 * inch,
        bottomMargin=0.62 * inch,
        title=label["title"],
        author="ToxiGuard-Platform Ver.1",
    )

    styles = _build_styles(getSampleStyleSheet(), ParagraphStyle, font_name, bold_font, colors, TA_CENTER, TA_LEFT)
    story: list[Any] = []
    context = payload.get("product_context") or {}
    profile = payload.get("application_snapshot") or {}
    worksheet = payload.get("fda_style_reviewer_worksheet") or {}
    integrated = _worksheet_item(worksheet, "Integrated Assessment")

    story.extend(_cover_page(payload, profile, context, integrated, styles, label, Table, TableStyle, colors, language))
    story.append(PageBreak())

    story.extend(_section(label["application_snapshot"], styles, label))
    story.append(
        _key_value_table(
            [
                (label["application_type"], profile.get("application_type")),
                (label["application_number"], profile.get("application_number")),
                (label["applicant"], profile.get("applicant")),
                (label["review_cycle"], profile.get("review_cycle")),
                (label["discipline_owner"], profile.get("discipline_owner")),
                (label["review_status"], profile.get("review_status")),
            ],
            styles,
            Table,
            TableStyle,
            colors,
        )
    )

    story.extend(_section(label["executive_summary"], styles, label))
    story.append(_para(_executive_narrative(payload, worksheet, label, language), styles["Body"]))
    story.append(Spacer(1, 8))
    story.append(
        _key_value_table(
            [
                (label["recommendation"], _localize(integrated.get("Recommended Action"), language)),
                (label["key_issues"], integrated.get("Key Review Issues")),
                (label["residual_uncertainty"], integrated.get("Residual Uncertainty")),
                (label["rationale"], integrated.get("Rationale")),
            ],
            styles,
            Table,
            TableStyle,
            colors,
        )
    )

    story.extend(_development_story_section(payload, context, worksheet, styles, label, Table, TableStyle, colors))
    story.extend(_product_context_section(context, styles, label, Table, TableStyle, colors))
    story.extend(_document_extraction_section(payload, styles, label, Table, TableStyle, colors))
    story.extend(_ctd_evidence_section(worksheet, styles, label, Table, TableStyle, colors))
    story.extend(_specification_section(payload, worksheet, styles, label, Table, TableStyle, colors))
    story.extend(_signal_section(label["performance"], payload.get("bioequivalence"), styles, label, "bioequivalence"))
    story.extend(_bioequivalence_calculation_section(payload, styles, label, Table, TableStyle, colors))
    story.extend(_signal_section(label["stability"], payload.get("stability"), styles, label, "stability"))
    story.extend(_impurity_section(payload, context, worksheet, styles, label, Table, TableStyle, colors))
    story.extend(_sources_section(payload, styles, label, Table, TableStyle, colors))
    story.extend(_deficiency_section(worksheet, styles, label, Table, TableStyle, colors))
    story.extend(_integrated_section(worksheet, styles, label, Table, TableStyle, colors))
    story.extend(_appendix_section(payload, styles, label, Table, TableStyle, colors))
    story.append(Spacer(1, 10))
    story.append(_para(label["disclaimer"], styles["FinePrint"]))

    def on_page(canvas, doc_obj):
        _draw_header_footer(canvas, doc_obj, label, colors, font_name, bold_font)

    def on_cover(canvas, doc_obj):
        _draw_cover_footer(canvas, doc_obj, label, colors, font_name)

    doc.build(story, onFirstPage=on_cover, onLaterPages=on_page)
    return buffer.getvalue()


def _register_report_fonts(pdfmetrics, TTFont, UnicodeCIDFont, language: str) -> tuple[str, str]:
    if language == "ko":
        for font_path in [
            "/System/Library/Fonts/Supplemental/AppleGothic.ttf",
            "/System/Library/Fonts/AppleSDGothicNeo.ttc",
        ]:
            if Path(font_path).exists():
                try:
                    pdfmetrics.registerFont(TTFont("ToxiKorean", font_path))
                    return "ToxiKorean", "ToxiKorean"
                except Exception:
                    continue
        try:
            pdfmetrics.registerFont(UnicodeCIDFont("HYSMyeongJo-Medium"))
            pdfmetrics.registerFont(UnicodeCIDFont("HYGothic-Medium"))
            return "HYSMyeongJo-Medium", "HYGothic-Medium"
        except Exception:
            pass
    return "Helvetica", "Helvetica-Bold"


