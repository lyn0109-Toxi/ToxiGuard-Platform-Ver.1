def _safe(value) -> str:
    return escape(_clean_text(value, 1800)).replace("\n", "<br/>")


def _localize(value, language: str):
    if value is None:
        return value
    if language != "ko":
        return _english_display_value(value)
    if isinstance(value, str):
        text = value
        for source, target in COMMON_KO_TERMS.items():
            text = text.replace(source, target)
        return _dedupe_slash_duplicates(text)
    return value


def _dedupe_slash_duplicates(value: str) -> str:
    parts = [part.strip() for part in re.split(r"\s*/\s*", value or "") if part.strip()]
    if len(parts) == 2 and parts[0] == parts[1]:
        return parts[0]
    return value


def _report_text(value, language: str, limit: int = 500) -> str:
    text = _clean_text(_localize(value, language), limit)
    if language == "en":
        text = _strip_hangul_for_english(text)
    return text


def _report_column_label(value: str, language: str) -> str:
    if language == "ko":
        return str(value)
    label = REPORT_COLUMN_LABELS.get(str(value), _english_display_value(str(value)))
    return _strip_hangul_for_english(_clean_text(label, 120))


def _english_display_value(value):
    if not isinstance(value, str):
        return value
    if not re.search(r"[가-힣]", value):
        return value
    parts = [part.strip() for part in re.split(r"\s*/\s*", value) if part.strip()]
    english_parts = [part for part in parts if not re.search(r"[가-힣]", part)]
    if english_parts:
        return english_parts[0]
    return _strip_hangul_for_english(value)


def _strip_hangul_for_english(value: str) -> str:
    text = re.sub(r"[가-힣]+", "", str(value or ""))
    text = re.sub(r"\s+", " ", text).strip(" /-;:,.")
    if re.search(r"[A-Za-z0-9]", text):
        return text
    return "Korean source text omitted in English report; verify source document."


def _signal_interpretation(category: str, rows: list[dict], label: dict) -> str:
    if not rows:
        return label["gap"]
    direct = sum(1 for row in rows if "Direct" in str(row.get("Evidence Role", "")))
    rationale = sum(1 for row in rows if "Rationale" in str(row.get("Evidence Role", "")))
    if _is_ko(label):
        return f"{len(rows)}개 근거 감지; 직접 근거 {direct}개, 개발/보조 근거 {rationale}개."
    return f"{len(rows)} evidence item(s); {direct} direct, {rationale} development/supporting rationale."


def _is_ko_style(styles: dict) -> bool:
    return "ToxiKorean" in getattr(styles["Body"], "fontName", "") or "HYS" in getattr(styles["Body"], "fontName", "")


def _clean_text(value, limit: int = 500) -> str:
    if isinstance(value, dict):
        value = "; ".join(f"{k}: {v}" for k, v in value.items())
    elif isinstance(value, (list, tuple)):
        value = "; ".join(str(item) for item in value)
    text = re.sub(r"\s+", " ", str(value or "")).strip()
    text = text.replace("{'", "").replace("'}", "")
    if len(text) > limit:
        return text[: limit - 3].rstrip() + "..."
    return text


def _limit_rows(rows, limit):
    if isinstance(rows, dict):
        rows = [rows]
    return list(rows or [])[:limit]


def _preferred_columns(rows, preferred):
    if preferred:
        available = []
        if rows:
            keys = set().union(*(row.keys() for row in rows if isinstance(row, dict)))
            available = [col for col in preferred if col in keys]
        if available:
            return available
    if rows and isinstance(rows[0], dict):
        return list(rows[0].keys())[:5]
    return ["Value"]


def _value_for_column(row, col):
    if isinstance(row, dict):
        return row.get(col, "")
    return row


def _worksheet_item(worksheet, key):
    if not isinstance(worksheet, dict):
        return {} if key != "Final Review Language" else ""
    value = worksheet.get(key)
    if value is None:
        return {} if key != "Final Review Language" else ""
    return value


def _normalize_basic_rows(rows, label):
    normalized = []
    for row in rows or []:
        normalized.append(
            {
                "항목" if _is_ko(label) else "Field": _localize(row.get("Field", ""), "ko" if _is_ko(label) else "en"),
                "값" if _is_ko(label) else "Value": _localize(row.get("Value", ""), "ko" if _is_ko(label) else "en"),
                label["reviewer_check"]: row.get("Reviewer Check", ""),
            }
        )
    return normalized


def _rename_rows(rows, mapping: dict, ko: bool):
    if not ko:
        return rows
    renamed = []
    for row in rows or []:
        renamed.append({target: _localize(row.get(source, ""), "ko") for source, target in mapping.items()})
    return renamed


def _is_ko(label):
    return label["title"].startswith("CTD 품질")


