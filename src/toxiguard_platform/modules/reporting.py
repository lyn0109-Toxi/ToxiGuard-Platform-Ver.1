def _cover_page(payload, profile, context, integrated, styles, label, Table, TableStyle, colors, language: str) -> list:
    from reportlab.platypus import Spacer

    product = context.get("product_name") or profile.get("product_name") or "TBD"
    active = context.get("active_substance") or "TBD"
    generated = f"{dt.datetime.utcnow():%Y-%m-%d %H:%M UTC}"
    meta_rows = [
        (label["product"], product),
        (label["active"], active),
        (label["strength"], context.get("strength") or "TBD"),
        (label["dosage"], context.get("dosage_form") or profile.get("dosage_form") or "TBD"),
        (label["route"], context.get("route") or profile.get("route") or "TBD"),
        (label["recommendation"], _localize(integrated.get("Recommended Action") or "TBD", language)),
        (label["generated"], generated),
    ]
    logo = Table(
        [[_para("TG", styles["CoverTitle"])]],
        colWidths=[72],
        rowHeights=[72],
        style=[
            ("BACKGROUND", (0, 0), (-1, -1), colors.HexColor("#102033")),
            ("TEXTCOLOR", (0, 0), (-1, -1), colors.white),
            ("ALIGN", (0, 0), (-1, -1), "CENTER"),
            ("VALIGN", (0, 0), (-1, -1), "MIDDLE"),
            ("BOX", (0, 0), (-1, -1), 0.8, colors.HexColor("#102033")),
        ],
    )
    return [
        Spacer(1, 40),
        logo,
        Spacer(1, 18),
        _para(label["title"], styles["CoverTitle"]),
        _para(label["subtitle"], styles["CoverSubtitle"]),
        Spacer(1, 28),
        _key_value_table(meta_rows, styles, Table, TableStyle, colors),
        Spacer(1, 26),
        _para(label["disclaimer"], styles["FinePrint"]),
    ]


def _section(title: str, styles: dict, label: dict) -> list:
    from reportlab.platypus import KeepTogether

    return [KeepTogether([_para(title, styles["Section"]), _rule()])]


