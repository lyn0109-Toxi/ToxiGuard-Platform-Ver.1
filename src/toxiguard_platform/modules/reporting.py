def _bullet_list(items, styles, label, limit=8):
    from reportlab.platypus import Paragraph

    items = items or []
    if isinstance(items, str):
        items = [items]
    if not items:
        return _para(label["not_detected"], styles["Body"])
    language = "ko" if _is_ko(label) else "en"
    text = "<br/>".join(f"• {_safe(_report_text(item, language, 360))}" for item in items[:limit])
    if len(items) > limit:
        text += f"<br/>• ... +{len(items) - limit}"
    return Paragraph(text, styles["Body"])


